import { createStore, combineReducers } from 'redux';
import { toggleLogin } from '../reducers';

let store;

export default {
  configure: (initialState) => {
    const reducers = combineReducers({
      toggleLogin
    });

    if (initialState) {
      store = createStore(
        reducers,
        initialState
      );
      return store;
    }

    store = createStore(
      reducers
    );

    return store;
  },

  currentStore: () => {
    return store;
  }
};

